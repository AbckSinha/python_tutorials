'''

The below sample shows how to declare variables in python

'''

counter = 100          # Creates an integer variable
miles = 1000.0       # Creates a floating point variable
name = "Zara Ali"   # Creates a string variable

print(counter)
print(miles)
print(name)

# delete a variable

counter = 100
print(counter)

del counter
print(counter)

a = b = c = 100

print(a)
print(b)
print(c)

a, b, c = 1, 2, "Zara Ali"

print(a)
print(b)
print(c)

